/**
 * Text-field wrappers that close the IME-input gap.
 *
 * The lock-aware modifier on dialogs/sheets/popups handles physical pointer
 * and key events but not soft-keyboard input: when the user types in Gboard,
 * the touches land on the IME's window and the resulting text is delivered to
 * our `TextField` via `InputConnection`, which generates *no* pointer or key
 * events on our side. Without an explicit ping in `onValueChange`, a user
 * typing for 30+ seconds straight (long secret, master password retype)
 * silently misses every reset and gets locked out mid-form.
 *
 * Each wrapper here intercepts `onValueChange` to ping
 * [LocalUserActivityPing] before forwarding to the caller's handler, then
 * additionally applies [Modifier.lockAware] to the field surface for the rare
 * cursor-tap / hardware-keyboard cases the typing-ping doesn't reach.
 *
 * Signatures mirror Material3 1.3.x (`OutlinedTextField` and `TextField`,
 * String + TextFieldValue overloads). Migration is import + name swap.
 *
 * Note: all four overloads ping unconditionally on every value change. Even
 * if the user pastes a long string (one event), or types char-by-char
 * (many events), the 5-second throttle inside
 * [com.onekey.core.security.AutoLockManager.onUserActivity] caps the actual
 * timer-reset rate — pinging cheaply per keystroke is fine.
 */

// ─────────────────────────────────────────────────────────────────────────────
// OutlinedTextField — String
// ─────────────────────────────────────────────────────────────────────────────

@Composable
fun LockAwareOutlinedTextField(
    value: String,
    onValueChange: (String) -> Unit,
    modifier: Modifier = Modifier,
    enabled: Boolean = true,
    readOnly: Boolean = false,
    textStyle: TextStyle = LocalTextStyle.current,
    label: @Composable (() -> Unit)? = null,
    placeholder: @Composable (() -> Unit)? = null,
    leadingIcon: @Composable (() -> Unit)? = null,
    trailingIcon: @Composable (() -> Unit)? = null,
    prefix: @Composable (() -> Unit)? = null,
    suffix: @Composable (() -> Unit)? = null,
    supportingText: @Composable (() -> Unit)? = null,
    isError: Boolean = false,
    visualTransformation: VisualTransformation = VisualTransformation.None,
    keyboardOptions: KeyboardOptions = KeyboardOptions.Default,
    keyboardActions: KeyboardActions = KeyboardActions.Default,
    singleLine: Boolean = false,
    maxLines: Int = if (singleLine) 1 else Int.MAX_VALUE,
    minLines: Int = 1,
    interactionSource: MutableInteractionSource? = null,
    shape: Shape = OutlinedTextFieldDefaults.shape,
    colors: TextFieldColors = OutlinedTextFieldDefaults.colors(),
) {
    val ping by rememberUpdatedState(LocalUserActivityPing.current)
    OutlinedTextField(
        value = value,
        onValueChange = { newValue ->
            ping()
            onValueChange(newValue)
        },
        modifier = modifier.lockAware(),
        enabled = enabled,
        readOnly = readOnly,
        textStyle = textStyle,
        label = label,
        placeholder = placeholder,
        leadingIcon = leadingIcon,
        trailingIcon = trailingIcon,
        prefix = prefix,
        suffix = suffix,
        supportingText = supportingText,
        isError = isError,
        visualTransformation = visualTransformation,
        keyboardOptions = keyboardOptions,
        keyboardActions = keyboardActions,
        singleLine = singleLine,
        maxLines = maxLines,
        minLines = minLines,
        interactionSource = interactionSource,
        shape = shape,
        colors = colors,
    )
}

